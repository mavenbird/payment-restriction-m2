namespace Mavenbird\Payrestriction\Controller\Adminhtml\Rule;

class Index extends \Mavenbird\Payrestriction\Controller\Adminhtml\Rule
{

    /**
     * Execute
     *
     * @return void
     */
    public function execute()
    {
        /** @var \Magento\Backend\Model\View\Result\Page $resultPage */
        $resultPage = $this->resultPageFactory->create();
        $resultPage->setActiveMenu('Mavenbird_Payrestriction::sales_restiction');
        $resultPage->addBreadcrumb(__('Rules'), __('Rules'));
        $resultPage->getConfig()->getTitle()->prepend(__('Payment Restrictions'));

        return $resultPage;
    }
}
